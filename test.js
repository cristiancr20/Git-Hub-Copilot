//Funcion para multiplicar dos numeros
function multiply(a, b) {
    //Se multiplican los dos numeros
    return a * b;
}


//Funcion para dividir dos numeros
function divide(a, b) {
    //Se dividen los dos numeros
    return a / b;
}

//Funcion para sumar dos numeros
function add(a, b) {
    //Se suman los dos numeros
    return a + b;
}


//Funcion para restar dos numeros
function subtract(a, b) {
    //Se restan los dos numeros
    return a - b;
}

